def main():
    filename = sys.argv[1] if len(sys.argv) > 1 else "../../clpoly/polynomial_factorize_zp.hh"
    args = ["-std=c++17", "-I../../"]

    funcs = parse_file(filename, args=args, target_file="factorize_zp")

    # 翻译 + UB 注入
    ssa_funcs = []
    for f in funcs:
        sf = transform_func(f)
        obs = collect_all(sf)
        inject_obligations(sf, obs)
        ssa_funcs.append(sf)

    # 按调用图排序：被调函数在前
    ordered = _topological_sort(ssa_funcs)

    # 输出：model + 翻译函数
    model_code = _read_model()
    print(model_code)
    print("-- ============================================================")
    print("-- 翻译函数（由 cpp2lean 自动生成）")
    print("-- ============================================================")
    print()
    for sf in ordered:
        print(gen_ssa_func(sf))
        print()


def _topological_sort(funcs):
    """按调用图拓扑排序：被调函数在调用者之前。"""
    name_to_func = {sf.name: sf for sf in funcs}
    translated = set(name_to_func.keys())

    # 构建依赖图
    deps = {}
    for sf in funcs:
        code = gen_ssa_func(sf)
        called = set()
        for m in re.findall(r'(\w+)_ir\b', code):
            if m in translated and m != sf.name:
                called.add(m)
        deps[sf.name] = called

    # Kahn's algorithm
    in_degree = {name: 0 for name in translated}
    for name, callees in deps.items():
        for callee in callees:
            if callee in in_degree:
                in_degree[callee] = in_degree.get(callee, 0)  # no increment for callee

    # 反转：如果 A 调用 B，B 的入度++（B 要先输出）
    reverse_deps = {name: set() for name in translated}
    for name, callees in deps.items():
        for callee in callees:
            if callee in reverse_deps:
                reverse_deps[callee].add(name)

    in_deg = {name: len(callees & translated) for name, callees in deps.items()}
    queue = [name for name, d in in_deg.items() if d == 0]
    result = []
    visited = set()

    while queue:
        name = queue.pop(0)
        if name in visited:
            continue
        visited.add(name)
        result.append(name_to_func[name])
        for dependent in reverse_deps.get(name, []):
            in_deg[dependent] -= 1
            if in_deg[dependent] <= 0 and dependent not in visited:
                queue.append(dependent)

    # 处理循环依赖（如递归函数）
    for sf in funcs:
        if sf.name not in visited:
            result.append(sf)

    return result


if __name__ == "__main__":
    main()
