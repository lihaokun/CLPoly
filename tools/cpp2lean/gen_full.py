import os

MODEL_PATH = os.path.join(os.path.dirname(__file__), "clpoly_model.lean")

def _read_model() -> str:
    """读取 clpoly_model.lean，去掉末尾的 #eval 测试。"""
    with open(MODEL_PATH) as f:
        lines = f.readlines()
    # 去掉 §6 验证测试部分
    result = []
    for line in lines:
        if line.strip().startswith("-- §6.") or line.strip().startswith("#eval"):
            break
        result.append(line)
    return "".join(result)


